public class PlayRestGetter {
    float play;
    float rest;

    public PlayRestGetter() {

    }

    public PlayRestGetter(float play, float rest) {
        this.play = play;
        this.rest = rest;
    }

    public float getPlay() {
        return play;
    }

    public float getRest() {
        return rest;
    }
}
